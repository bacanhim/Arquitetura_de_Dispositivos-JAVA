    private void receiveMessages(EventHubClient ehClient, String partitionId) throws EventHubException {
        final ExecutorService executorService = Executors.newSingleThreadExecutor();

        // Create the receiver using the default consumer group.
        // For the purposes of this sample, read only messages sent since 
        // the time the receiver is created. Typically, you don't want to skip any messages.
        ehClient.createReceiver(EventHubClient.DEFAULT_CONSUMER_GROUP_NAME, partitionId,
                EventPosition.fromEnqueuedTime(Instant.now())).thenAcceptAsync(receiver -> {

            while (true) {
                try {
                    Iterable<EventData> receivedEvents = receiver.receiveSync(100);
                    if (receivedEvents != null) {
                        for (EventData receivedEvent : receivedEvents) {
                            String alarme = "";
                            String str = (new String(receivedEvent.getBytes(), Charset.defaultCharset()));
                            Object obj = (Object) (new String(receivedEvent.getBytes(), Charset.defaultCharset()));
                            JsonParser parser = new JsonParser();
                            JsonObject object = (JsonObject) parser.parse((String) obj);
                            if (object.get("alarme").getAsBoolean()) {
                                alarme = "Verdadeiro";
                            } else {
                                alarme = "Falso";
                            }
                            jTextAreaLog.append("\nDist√¢ncia: " + object.get("distance") + "cm, Alerta: " + alarme + ", Data: " + object.get("data"));
                        }
                    }
                } catch (EventHubException e) {
                    jTextAreaLog.append("Error reading EventData");
                }
            }
        }, executorService);
    }
